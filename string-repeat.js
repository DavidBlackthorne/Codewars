// Challenge: Write a function that accepts a non-negative integer n and a string s as parameters, 
// and returns a string of s repeated exactly n times.

// parameters: integer will always be positive. 

// return: return as a concatenated string.

// Example: function (4, 'hello') = 'hellohellohellohello'

// pseudocode: use string repeat method for 'n' times.

// My solution:

function repeatStr (n, s) {
  return s.repeat(n);
}



